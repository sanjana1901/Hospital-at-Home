<p><font size="4px">Homeopathy is a medical system based on the belief that the body can cure itself. Those who practice it use tiny amounts of natural substances, like plants and minerals. They believe these stimulate the healing process.</font></p><BR>

<h3><a id="homeo1"><font size="5px">How Does It Work</font></a></h3>
<p><font size="4px">A basic belief behind homeopathy is “like cures like.” In other words, something that brings on symptoms in a healthy person can -- in a very small dose -- treat an illness with similar symptoms. This is meant to trigger the body’s natural defenses. </font></p>
<p><font size="4px">For example, red onion makes your eyes water. That’s why it’s used in homeopathic remedies for allergies. Treatments for other ailments are made from poison ivy, white arsenic, crushed whole bees, and an herb called arnica.</font></p><BR>

